n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro: '))
print('A soma vale \n {}'.format(n1+n2))
print('A subtração vale {}.'.format(n1-n2))
print('A multiplicação é {}.'.format(n1*n2), end=' ')
print('A divisão é {:.3f}'.format(n1/n2))

